
let a =3, b =2
let z = a + b
console.log("Resultado de la suma: " + z) 

z = a - b
console.log("Resultado de la resta: " + z)

z = a * b
console.log("Resultado de la multiplicación: " + z)

z = a / b
console.log("Resultado de la división: " + z)

z = a % b
console.log("Resultado de la operación módulo (residuo): " + z)

z = a ** b
console.log("Resultados de operador exponente: " + z)

//Incremento
//Pre-incremento (el operador ++ anres de la variable)
z = ++a
console.log(a)
console.log(z)
//Post-incremento (el operador ++ después de la variable)
z = b++
console.log(b)
console.log(z)

//Decremento
//Pre-decremento
z = --a
console.log(a)
console.log(z)

//Post-decremento
z = b--
console.log(b)
console.log(z)